    public void initFields() {
        //TEXT FIELDS
        tf_pesquisa.setStaticIcon("zoom.png", 20, 20);
        //TABLE
        List<List<?>> listas = new ArrayList();
        classes = new ArrayList();
        try {
            List<Fornecedor> f = new ArrayList();
            fornecedores = new FornecedorController().consultaFornecedorInfosByIdAtivo();
            for (FornecedorInfo finfo : fornecedores) {
                f.add(finfo.getIdFornecedor());
            }
            listas.add(f);
            listas.add(fornecedores);
            classes.add(Fornecedor.class);
            classes.add(FornecedorInfo.class);
        } catch (Exception ex) {
            Logger.getLogger(SelectFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbl_pesquisa.setModelo(new HMMultipleClassesTableModel(listas, classes, colunas));
        tbl_pesquisa.ocultarColunas(ocultas);
        tf_fornecedor.setEditable(false);
        tf_fornecedor.setEnabled(false);
    }

    public void onKeyReleased() {
        new Thread() {
            public void run() {
                tbl_pesquisa.getModeloMC().setListas(new FornecedorController().filtraFornecedores(tf_pesquisa.getText(), fornecedores));
            }
        }.start();
    }

    public void showFornecedor() {
        FornecedorInfo fi = null;
        List<?> l = tbl_pesquisa.getSelectedItemMC();
        for (Object ob : l) {
            if (ob instanceof FornecedorInfo) {
                fi = (FornecedorInfo) ob;
            }
        }
        try {
            tf_fornecedor.setText(fi.getIdFornecedor().getNome() + " - " + fi.getIdFornecedor().getCnpj());
            selectedFornecedor = fi;
        } catch (Exception ex) {
            Logger.getLogger(SelectFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void selectFornecedor() {
        if (!tf_fornecedor.getText().isEmpty()) {
            try {
                spe.addFornecedor(selectedFornecedor);
                this.dispose();
            } catch (Exception e) {
                Logger.getLogger(SelectFornecedor.class.getName()).log(Level.SEVERE, null, e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um fornecedor!");
        }
    }

    public SelectFornecedor(SelectProdutoExterno spe) {
        initComponents();
        this.spe = spe;
        new Thread() {
            public void run() {
                initFields();
            }
        }.start();
    }

