    public void initFields() {
        //TEXT FIELDS
        tf_pesquisa.setStaticIcon("zoom.png", 20, 20);
        //TABLE
        List<List<?>> listas = new ArrayList();
        classes = new ArrayList();
        try {
            List<Mecanico> m = new ArrayList();
            mecanicos = new MecanicoController().consultaMecanicoInfosByIdAtivo();
            for (MecanicoInfo minfo : mecanicos) {
                m.add(minfo.getIdMecanico());
            }
            listas.add(m);
            listas.add(mecanicos);
            classes.add(Mecanico.class);
            classes.add(MecanicoInfo.class);
        } catch (Exception ex) {
            Logger.getLogger(SearchMecanico.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbl_pesquisa.setModelo(new HMMultipleClassesTableModel(listas, classes, colunas));
        tbl_pesquisa.ocultarColunas(ocultas);
    }

    public void onKeyReleased() {
        new Thread() {
            public void run() {
                tbl_pesquisa.getModeloMC().setListas(new MecanicoController().filtraMecanicos(tf_pesquisa.getText(), mecanicos));
            }
        }.start();
    }

    public void rebuildMecanico() {
        MecanicoInfo mi = null;
        List<?> l = tbl_pesquisa.getSelectedItemMC();
        for (Object ob : l) {
            if (ob instanceof MecanicoInfo) {
                mi = (MecanicoInfo) ob;
            }
        }
        try {
            RegisterMecanico cm = new RegisterMecanico(mi, this, tbl_pesquisa.getSelectedRow());
            HMInternalFrame frame = new HMInternalFrame();
            frame.setSize(cm.getSize().width + 30, cm.getSize().height);
            frame.add(cm);
            frame.setVisible(true);
            Main.desktop.addInternalFrame(frame);
        } catch (Exception ex) {
            Logger.getLogger(RegisterMecanico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void alterObject(List<?> lista, int index) {
        tbl_pesquisa.alterObjectMC(lista, index);
    }

