    public void initFields() {
        //TEXT FIELDS
        tf_pesquisa.setStaticIcon("zoom.png", 20, 20);
        //TABLE
        List<List<?>> listas = new ArrayList();
        classes = new ArrayList();
        try {
            List<Cliente> c = new ArrayList();
            clientes = new ClienteBean().consultaClienteInfosByIdAtivo();
            for (ClienteInfo cinfo : clientes) {
                c.add(cinfo.getIdCliente());
            }
            listas.add(c);
            listas.add(clientes);
            classes.add(Cliente.class);
            classes.add(ClienteInfo.class);
        } catch (Exception ex) {
            Logger.getLogger(SearchCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbl_pesquisa.setModelo(new HMMultipleClassesTableModel(listas, classes, colunas));
        tbl_pesquisa.ocultarColunas(ocultas);
    }

    public void onKeyReleased() {
        new Thread() {
            public void run() {
                tbl_pesquisa.getModeloMC().setListas(new ClienteController().filtraClientes(tf_pesquisa.getText(), clientes));
            }
        }.start();
    }

    public void rebuildCliente() {
        ClienteInfo ci = null;
        List<?> l = tbl_pesquisa.getSelectedItemMC();
        for (Object ob : l) {
            if (ob instanceof ClienteInfo) {
                ci = (ClienteInfo) ob;
            }
        }
        try {
            RegisterCliente cc = new RegisterCliente();
            HMInternalFrame frame = new HMInternalFrame();
            frame.setSize(cc.getSize().width + 30, cc.getSize().height);
            frame.add(cc);
            Main.desktop.addInternalFrame(frame);
        } catch (Exception ex) {
            Logger.getLogger(RegisterCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void alterObject(List<?> lista, int index) {
        tbl_pesquisa.alterObjectMC(lista, index);
    }

